void Background::draw(sf::RenderWindow& window) const {
    sf::RectangleShape tile(sf::Vector2f(TILE_SIZE - 1.0f, TILE_SIZE - 1.0f));
    tile.setFillColor(sf::Color(0, 0, 255));

    for (int y = 0; y < MAP_HEIGHT; ++y) {
        for (int x = 0; x < MAP_WIDTH; ++x) {
            if (map[y][x] == 1) {
                tile.setPosition({ x * TILE_SIZE + 25.f, y * TILE_SIZE + 50.f }); 
                window.draw(tile);
            }
        }
    }
}

bool Background::isWall(float worldX, float worldY) const {
    int gridX = static_cast<int>((worldX - 25.f) / TILE_SIZE);
    int gridY = static_cast<int>((worldY - 50.f) / TILE_SIZE);
    if (gridX >= 0 && gridX < MAP_WIDTH && gridY >= 0 && gridY < MAP_HEIGHT)
        return map[gridY][gridX] == 1;
    return true;
}